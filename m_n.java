import java.util.Scanner;
public class m_n {
	public static void main(String[ ] args) {
		int m, n;
		Scanner sc = new Scanner(System.in);
	System.out.println("Введіть перемінну m:");
		m = sc.nextInt();
		if (m > 0){ n=1;
      }  else if (m==0)  { n=0;
	  } 	  else { n=-1;
		 	 }
		System.out.println("Значення перемінної n="+n);
 }
}